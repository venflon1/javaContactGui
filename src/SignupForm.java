    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        try {
            Connection conn = DBManager.getConnection();
            String sqlQuery = "INSERT INTO user(fname, lname, username, pass, pic)"
                       +"VALUE(?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sqlQuery);
            pstmt.setString(1, this.jTextFieldFistName.getText());
            pstmt.setString(2, this.jTextFieldLastName.getText());
            pstmt.setString(3, this.jTextFielUsername.getText());
            pstmt.setString(4, this.jPasswordPassword.toString());
            
            InputStream img = new FileInputStream(new File(this.imgPath));
            pstmt.setBlob(5, img);
            
            if( (pstmt.executeUpdate()) > 0){
                JOptionPane.showMessageDialog(null, "Account created");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(SignupForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(SignupForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

