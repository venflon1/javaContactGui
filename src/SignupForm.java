    private void jButtonScegliImmagineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonScegliImmagineActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        // file extension
        FileNameExtensionFilter fileFilter = new FileNameExtensionFilter("*.Images", "jpg", "png", "gif");
        fileChooser.addChoosableFileFilter(fileFilter);
        
        int fileState = fileChooser.showSaveDialog(null);
        
        // if the user select a file
        if(fileState == JFileChooser.APPROVE_OPTION){
            File selectedFile =  fileChooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            this.imgPath = path;
            
            // display the image into jlabel
            // this.jLabelShowPicture.setIcon(new ImageIcon(path));
            this.jLabelShowPicture.setIcon(this.resizePic(path));
        }
        else if(fileState == JFileChooser.CANCEL_OPTION){
            System.out.println("nessuna immagine selezionata!");
        }
                
    }//GEN-LAST:event_jButtonScegliImmagineActionPerformed

